Укажите отличия в системах репликации MongoDB и Redis.

 В MongoDB обмен изменениями происходит только с участием master-узла  +
 В Redis механизм репликации позволяет работать СУБД без master-узла 
 В MongoDB есть встроенный механизм решения проблем с отказом master-узла  +
 Механизмы репликации индентичны
