Ограниченные и неограниченные курсорные переменные 

Ограниченные курсорные переменные (constrained) объявляются только для конкретного возвращаемого типа.

Переменная должна открываться для такого запроса, список выбора которого соответствует типу, возвращаемому курсором.
В противном случае возникает предопределенная исключительная ситуация ROWTYPE_MISMATCH.

Для неограниченных курсорных переменных (unconstrained) предложение RETURN отсутствует.
Такая переменная может быть открыта для любого запроса.

DECLARE 
  --Определим неограниченный ссылочный тип
TYPE t_FlexibleRef IS REF CURSOR;
  -- и переменную этого типа
v_CursorVar t_FlexibleRef;

